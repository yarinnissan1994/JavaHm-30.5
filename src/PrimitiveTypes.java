public class PrimitiveTypes {
    byte verySmallNum = 10;
    short smallNum = 10000;
    int mediumNum = 10000000;
    long largeNum = 1000000000L;
    float shortDecimalNum = 10.99F;
    double longDecimalNumber = 10.99999999;
    char symbol = '$';
    boolean trueOrFalse = true;

    public void run(){
        System.out.println("byte example: "+verySmallNum);
        System.out.println("short example: "+smallNum);
        System.out.println("int example: "+mediumNum);
        System.out.println("long example: "+largeNum);
        System.out.println("float example: "+shortDecimalNum);
        System.out.println("double example: "+longDecimalNumber);
        System.out.println("char example: "+symbol);
        System.out.println("boolean example: "+trueOrFalse);
    }

}
